function fixFile(filePath) {
  const content = readFileSync(filePath, 'utf-8');
  const s = content.indexOf('['), e = content.lastIndexOf(']');
  if (s === -1 || e === -1) return;
  
  let words = JSON.parse(content.slice(s, e + 1));
  let changed = 0;
  
  for (const entry of words) {
    if (!entry.definition) continue;
    let def = entry.definition;
    const orig = def;
    
    // Apply phrase fixes first
    for (const [phrase, replacement] of PHRASE_FIXES) {
      const idx = def.toLowerCase().indexOf(phrase.toLowerCase());
      if (idx !== -1) {
        def = def.slice(0, idx) + replacement + def.slice(idx + phrase.length);
      }
    }
    
    // Apply word fixes
    for (const [word, replacement] of Object.entries(W)) {
      if (!replacement) continue; // skip empty replacements
      const escaped = word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
      const regex = new RegExp(`\\b${escaped}\\b`, 'gi');
      if (regex.test(def)) {
        def = def.replace(regex, (match) => {
          // Preserve leading capital
          if (match[0] === match[0].toUpperCase() && replacement[0] === replacement[0].toLowerCase()) {
            return replacement[0].toUpperCase() + replacement.slice(1);
          }
          return replacement;
        });
      }
    }
    
    // Cleanup
    def = def.replace(/\s+/g, ' ').trim();
    def = def.replace(/\ba a\b/g, 'a');
    def = def.replace(/\s+([,;.])/g, '$1');
    // Fix double articles
    def = def.replace(/\bthe the\b/gi, 'the');
    def = def.replace(/\ban an\b/gi, 'an');
    
    if (def !== orig) {
      entry.definition = def;
      changed++;
    }
  }
  
  if (changed > 0) {
    writeFileSync(filePath, content.slice(0, s) + JSON.stringify(words) + content.slice(e + 1));
  }
  
  const result = execSync(`node coca-verify.mjs "${filePath}" 2>&1`, { encoding: 'utf-8' });
  const pass = result.match(/PASS: (\d+) \(([^)]+)\)/);
  const flag = result.match(/FLAGGED: (\d+)/);
  console.log(`${filePath}: ${changed} fixed → PASS: ${pass?.[2]||'?'}, FLAGGED: ${flag?.[1]||'0'}`);
}

const files = process.argv.slice(2);
for (const f of files) fixFile(f);
